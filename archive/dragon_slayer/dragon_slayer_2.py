    print(f"  Under the Z_3 x Z_3 quotient:")
    print(f"    g1 acts on Curve 1 (the g2-fixed curve) by permuting (x_0,x_4,x_5)")
    print(f"    g2 acts on Curve 2 (the g1-fixed curve) by permuting (x_1,x_2,x_3)")
    print(f"    Each curve maps to a single singular point in the quotient")
    print(f"")
    print(f"  After resolution, the orbifold Euler characteristic changes:")
    print(f"    chi(orbifold) = chi(CY)/|G| = {chi}/9 = {chi//9}")
