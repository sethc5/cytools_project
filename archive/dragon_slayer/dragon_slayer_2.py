    # Under g1^2 = (0,5,4): same fixed locus as g1.
    
    print(f"  Distinct fixed curves:")
    print(f"    Curve 1: from g2, g2² (cycle 1,2,3) - cubic in P2 (genus 1)")
    print(f"    Curve 2: from g1, g1² (cycle 0,4,5) - cubic in P2 (genus 1)")
